package Fundamentos;

public class Exercicio3 {
public static void main(String[] args) {
	int a = 10;
	int b = -10;
	boolean c = true;
	boolean d = false;
	System.out.println(~a); // inverte o sinal e subtarai 1
	System.out.println(~b);  
	System.out.println(!c); // oposto ao valor booleano
	System.out.println(!d);
}
}
